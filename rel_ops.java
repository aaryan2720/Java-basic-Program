public class rel_ops {
    public static void main(String[] args) {
        int a = 15;
        int b = 12;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        

    }
}
